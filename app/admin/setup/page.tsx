"use client"

import { useQuery, useMutation } from "convex/react"
import { api } from "@/convex/_generated/api"
import { useRouter } from "next/navigation"
import { Button } from "@/components/ui/button"
import { Input } from "@/components/ui/input"
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { useState } from "react"

/**
 * Admin Setup — requires ADMIN_SETUP_KEY to claim
 *
 * The setup script generates a random key and tells you to set it in Convex.
 * You enter it here once to become admin. Prevents random users from claiming.
 */
export default function AdminSetupPage() {
  const router = useRouter()
  const hasAdmin = useQuery(api.admin.hasAdmin)
  const claimAdmin = useMutation(api.admin.claimAdmin)
  const [setupKey, setSetupKey] = useState("")
  const [claiming, setClaiming] = useState(false)
  const [error, setError] = useState("")

  async function handleClaim(e: React.FormEvent) {
    e.preventDefault()
    if (!setupKey.trim()) return
    setClaiming(true)
    setError("")
    try {
      await claimAdmin({ setupKey: setupKey.trim() })
      router.push("/admin")
    } catch (err) {
      setError(err instanceof Error ? err.message : "Failed to claim admin")
      setClaiming(false)
    }
  }

  if (hasAdmin === undefined) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <p className="text-muted-foreground">Loading...</p>
      </div>
    )
  }

  if (hasAdmin) {
    return (
      <div className="min-h-screen flex items-center justify-center px-4">
        <Card className="w-full max-w-md">
          <CardHeader>
            <CardTitle>Admin Already Configured</CardTitle>
            <CardDescription>
              An admin account already exists. Contact the site admin if you need access.
            </CardDescription>
          </CardHeader>
          <CardContent>
            <Button onClick={() => router.push("/dashboard")} className="w-full">
              Go to Dashboard
            </Button>
          </CardContent>
        </Card>
      </div>
    )
  }

  return (
    <div className="min-h-screen flex items-center justify-center px-4">
      <Card className="w-full max-w-md">
        <CardHeader>
          <CardTitle>Admin Setup</CardTitle>
          <CardDescription>
            Enter the admin setup key to become the site administrator.
            This is the ADMIN_SETUP_KEY from your Convex environment variables.
          </CardDescription>
        </CardHeader>
        <CardContent>
          <form onSubmit={handleClaim} className="space-y-4">
            {error && (
              <div className="bg-destructive/10 text-destructive text-sm p-3 rounded-md">
                {error}
              </div>
            )}
            <Input
              type="password"
              placeholder="Enter setup key"
              value={setupKey}
              onChange={(e) => setSetupKey(e.target.value)}
              autoComplete="off"
            />
            <Button type="submit" disabled={claiming || !setupKey.trim()} className="w-full">
              {claiming ? "Setting up..." : "Claim Admin Access"}
            </Button>
            <p className="text-xs text-muted-foreground text-center">
              Generated by <code className="bg-muted px-1 rounded">bash scripts/setup.sh</code> — or set manually with <code className="bg-muted px-1 rounded">npx convex env set ADMIN_SETUP_KEY</code>
            </p>
          </form>
        </CardContent>
      </Card>
    </div>
  )
}
